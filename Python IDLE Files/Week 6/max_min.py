#Function max and min
big = max('Hello world')        # ('Hello world') is the ARGUMENT - "max" is a internal python function which "returns" the "max" charachter in the argument. 
print(big)                      # In this case it will print out or "return" a "W" which is the "big" in Hello World - of A-Z

tiny = min('Hello world')       # ('Hello world') is the ARGUMENT - "min" is a internal python function which "returns" the "min" charachter in the argument. 
print(tiny)                     # In this case it will print out or "return" a "space" which is the "min" in Hello World - "space"(no charachter) is the smallest charachter

