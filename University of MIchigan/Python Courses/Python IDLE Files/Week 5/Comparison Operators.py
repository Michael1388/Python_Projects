#Comparison Operators
x = 5
if x == 5 :
    print('Equals 5') 
if x > 4 : 
    print('Greater than 4')
if x >= 5 :
    print('Greater than or Equals 5')
#if only one line of code you can run it all in one line
if x < 6 : print('Less than 6')
if x <= 5 :
    print('Less than or Equals 5')
if x != 6 :
    print('Not equal to 6')

